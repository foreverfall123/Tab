package com.namseoul.sa.tab;

public class GPSData {
    private Double lo;
    private Double la;
    private String na;

    public void setlo(Double lo){
        this.lo = lo;
    }
    public void setla(Double la){
        this.la = la;
    }
    public void setna(String na){
        this.na = na;
    }
    public Double getlo(){
        return lo;
    }
    public Double getla(){
        return la;
    }
    public String getna(){
        return na;
    }
}
